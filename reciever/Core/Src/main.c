void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
    if (GPIO_Pin == GPIO_PIN_8) { // Check if the interrupt is from PB8
        HAL_Delay(50); // Simple debouncing (50 ms delay)
        if (HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_8) == GPIO_PIN_SET) {
            button_pressed = 1; // Register the button press
        }
    }
}

void Buzzer_On(uint32_t frequency)
{
    // Set the PWM frequency
    uint32_t timerClock = HAL_RCC_GetPCLK2Freq(); // TIM1 runs on APB2 clock
    uint32_t prescaler = (timerClock / 1000000) - 1; // Scale to 1 MHz
    uint32_t period = 1000000 / frequency - 1; // Calculate period for desired frequency

    htim1.Instance->PSC = prescaler;        // Set prescaler
    htim1.Instance->ARR = period;          // Set auto-reload value
    htim1.Instance->CCR2 = period / 2;     // Set 50% duty cycle for Channel 2

    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
}

